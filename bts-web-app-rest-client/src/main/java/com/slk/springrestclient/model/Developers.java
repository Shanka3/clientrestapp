package com.slk.springrestclient.model;

public class Developers {

	private int devId;

	private String devName;

	private String devBugModuleType;

	public Developers() {
	}

	public int getDevId() {
		return devId;
	}

	public void setDevId(int devId) {
		this.devId = devId;
	}

	public String getDevName() {
		return devName;
	}

	public void setDevName(String devName) {
		this.devName = devName;
	}

	public String getDevBugModuleType() {
		return devBugModuleType;
	}

	public void setDevBugModuleType(String devBugModuleType) {
		this.devBugModuleType = devBugModuleType;
	}

}
